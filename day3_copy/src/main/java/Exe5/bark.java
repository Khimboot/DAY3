package Exe5;

public class bark {

	public void bark() {
		System.out.println("Dog is barking");
	}
	public void bark(String a) {
		System.out.println("Dog is howling");
	}
	public void bark(int a) {
		System.out.println("Dog is having fear barking");
	}
	public void bark(boolean a) {
		System.out.println("Dog is having excitement barking");
	}
}
